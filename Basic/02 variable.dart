void main (){
  // Explicit typing
  int age = 25;
  String name = "John Doe";
  //Implicit typing with 'var'
  var city = "New Yok";
  // Using "Final" (immutable after initialization)
  final country = "USA";
  //Using "Const" (Compile time constant)
  const pi = 3.14159;
  print('Name : $name, Age : $age, City: $city, Country: $country, PI: $pi');
}